    public function execute($entrada) {
        $tokenAux = ""; // Armazena os caracteres do token atual
        $listatokens = []; // Lista para armazenar os tokens gerados
        $listatokensInvalidos = [];
        $valor = ""; // Valor temporário do token
        $estado = 'q0'; // Estado inicial
        // $entrada = $this->validate(); // Entrada a ser analisada
        
        $delta = $this->delta; // Função de transição de estados
        $aux = $this->aux; // Map de estados finais para os tokens
        $finais = $this->finais; // Lista de estados finais
    
        $line = 1; // Inicializa a linha
        $column = 0; // Inicializa a coluna
    
        for ($i = 0; $i < strlen($entrada); $i++) { // Itera sobre a string de entrada
            $nextChar = isset($entrada[$i + 1]) ? $entrada[$i + 1] : null; // Próximo caractere
            
            // Se o caractere atual for uma nova linha, atualiza a linha e redefine a coluna
            if ($entrada[$i] === "\n") {
                $line++; // Incrementa a linha
                $column = 1; // Reseta a coluna para 1 na nova linha
                continue; // Salta para a próxima iteração
            }
    
            // Acumula o caractere atual
            $tokenAux .= $entrada[$i];
            
            // Verifica se há uma transição no estado atual com o caractere atual
            if (isset($delta[$estado]) && isset($delta[$estado][$entrada[$i]])) {  
                $estado = $delta[$estado][$entrada[$i]]; // Transição de estado
                $valor .= $entrada[$i]; // Acumula o valor do token
                $column++; // Incrementa a coluna
            }
            
            // Verifica se não há transição para o próximo caractere (fim de um token)
            if (!isset($delta[$estado][$nextChar])) {
    
                // Verifica se o estado atual é final (token válido)
                if (in_array($estado, $finais)) { 
                    $tok = $aux[$estado]; // Obtém o nome do token
                    
                    // Adiciona o token à lista
                    $tempToken = new Token($tok, $valor);
                    $listatokens[] = $tempToken;      
    
                    // Reseta valores para o próximo token
                    $valor = "";
                    $estado = 'q0'; // Volta para o estado inicial
                    $tokenAux = ""; // Reseta o acumulador de token
                } elseif (!ctype_space($entrada[$i])) { // Verifica se o caractere atual não é espaço em branco
                    // Adiciona o token inválido com a posição do caractere

                    $listatokensInvalidos[] = new Token('INVALID', $tokenAux . " (Linha: $line, Coluna: $column)");
                    $tokenAux = ""; // Reseta tokenAux
                    $estado = 'q0'; // Reseta o estado
                    $valor = ""; // reseta o lexema 
                } 
                
                // Incrementa a coluna após processar o caractere
                $column++; 
            } 
        }
    
        return $listatokens;
    }

    public function getListaTokens(){
        return $this->listatokens;
    }
    


